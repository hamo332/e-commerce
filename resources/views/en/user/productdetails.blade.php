        });

        $(document).on('keyup', '.search-input', function(e) {
            e.preventDefault();

            var input_value = $(this).val();

            if (input_value) {
                $.ajax({
                    url: "http://carsrental.great-site.net/en/live-search",
                    type: 'GET',
                    data: {
                        input_value: input_value
                    },
                    beforeSend: function(data) {
                        $('.search-button-submit').attr('disabled', true);
                        $('.search-button-submit svg').removeClass('fa-search fa-fw');
                        $('.search-button-submit svg').addClass('fa-spinner fa-spin fa-fw');
                    },
                    success: function(data) {
                        $('.search-content').html(data);
                        $('.search-content').fadeIn();
                        $('.search-button-submit svg.fa-spinner').remove();
                        $('.search-button-submit').html('Search' +
                            '<i class="fa fa-search fa-fw"></i>');
                        $('.search-button-submit').attr('disabled', false);
                    }
                });
            } else {
                $('.search-content').fadeOut();
            }

        });

        $('.search-input').blur(function() {
            $('.search-box').fadeOut();
        });

    });
